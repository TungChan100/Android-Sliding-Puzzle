    private static int[] createRandomArray(int[] arr1) {

        int size = arr1.length;
        int[] arr2 = new int[size];

        LinkedList<Integer> list = new LinkedList<Integer>();

        for (int i = 0; i < size; i++) {
            list.add(arr1[i]);
        }

        for (int i = 0; i < size; i++) {
            int listSize = list.size();
            int toRemoveIndex = (int) (Math.random() * listSize);
            arr2[i] = list.get(toRemoveIndex);
            list.remove(toRemoveIndex);
        }


        return arr2;
    }

